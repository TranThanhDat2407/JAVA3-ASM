    public void fillInfoToForm(int index) {
        if (index >= 0 && index < listStudents.size()) {
            Students student = listStudents.get(index);
            txtName.setText(student.getName());
            txtAddress.setText(student.getAddress());
            txtParentName.setText(student.getParentName());
            txtContact.setText(student.getPhone());

            String standard = student.getStandard();
            if (standard != null) {
                cboStandard.setSelectedItem(standard);

                for (standards std : listStandards) {
                    if (std.getStandard().equals(standard)) {
                        String fees = std.getFees();
                        if (fees != null) {
                            cboFees.setSelectedItem(fees);
                        }
                        break;
                    }
                }
            }
        }
    }

