    public void initCBO() {
        try {
            Connection conn = DriverManager.getConnection(DB_URL, USERNAME, PASSWORD);
            Statement stmt = conn.createStatement();
            String SQL = "SELECT * FROM standards";
            ResultSet rs = stmt.executeQuery(SQL);

            cboStandard.removeAllItems();
            cboFees.removeAllItems();

            while (rs.next()) {
                cboStandard.addItem(rs.getString("standard"));
                cboFees.addItem(rs.getString("fees"));
            }
            conn.close();
        } catch (Exception e) {
            e.printStackTrace();
        }
    }

    public void fillTable() {
        DefaultTableModel model = (DefaultTableModel) tblStudents.getModel();
        model.setRowCount(0);
        for (Students e : listStudents) {
            model.addRow(new Object[]{e.getName(), e.getStandard()});
        }
    }

    private void loadDBtoArray() {
        try {
            Connection conn = DriverManager.getConnection(DB_URL, USERNAME, PASSWORD);
            Statement stmt = conn.createStatement();
            String SQL = "SELECT * FROM Students";
            ResultSet rs = stmt.executeQuery(SQL);
            listStudents.clear();
            while (rs.next()) {
                listStudents.add(new Students(rs.getInt(1), rs.getString(2), rs.getString(3), rs.getString(4), rs.getString(5), rs.getString(6), rs.getString(7)));
            }

            String standardsSQL = "SELECT * FROM standards";
            ResultSet standardsRS = stmt.executeQuery(standardsSQL);
            listStandards.clear();
            while (standardsRS.next()) {
                listStandards.add(new standards(standardsRS.getString(1), standardsRS.getString(2)));
            }

            conn.close();

        } catch (Exception e) {
            e.printStackTrace();
        }
    }

