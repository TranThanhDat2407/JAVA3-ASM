    private void setDisplay(int i) {
        STUDENTS e = listStudents.get(i);
        txtMaSV.setText(e.getMaSV());
        txtHoten.setText(e.getHoten());
        txtEmail.setText(e.getEmail());
        txtSoDT.setText(e.getSoDT());
        txtDiachi.setText(e.getDiachi());
        if (e.isGioitinh()) {
            rdoNam.setSelected(true);
        } else {
            rdoNu.setSelected(true);
        }
    }

    private void loadDBtoArray() {
        try {
            Connection conn = DriverManager.getConnection(DB_URL, USERNAME, PASSWORD);
            Statement stmt = conn.createStatement();
            String SQL = "select * from STUDENTS";
            ResultSet rs = stmt.executeQuery(SQL);
            listStudents.clear();
            while (rs.next()) {
                String MaSV = rs.getString(1);
                String Hoten = rs.getString(2);
                String Email = rs.getString(3);
                String SoDT = rs.getString(4);
                boolean Gioitinh = rs.getBoolean(5);
                String Diachi = rs.getString(6);
                listStudents.add(new STUDENTS(MaSV, Hoten, Email, SoDT, Gioitinh, Diachi));
            }
            conn.close();
        } catch (Exception e) {
            e.printStackTrace();
        }
    }
