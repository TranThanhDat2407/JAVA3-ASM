    private void txtFindActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_txtFindActionPerformed
        String searchMaSV = txtFind.getText().trim();

        if (!searchMaSV.isEmpty()) {
            findStudentByMaSV(searchMaSV);
        } else {
            setDisplay(0);
        }
    }//GEN-LAST:event_txtFindActionPerformed

    private void findStudentByMaSV(String searchMaSV) {
        boolean found = false;
        for (STUDENTS student : listStudents) {
            if (student.getMaSV().equalsIgnoreCase(searchMaSV)) {
                found = true;
                txtMaSV.setText(student.getMaSV());
                txtHoten.setText(student.getHoten());
                txtEmail.setText(student.getEmail());
                if (student.isGioitinh()) {
                    rdoNam.setSelected(true);
                } else {
                    rdoNu.setSelected(true);
                }
                txtDiachi.setText(student.getDiachi());

            }
        }

        if (!found) {
            JOptionPane.showMessageDialog(this, "Không tìm thấy MaSV.", "Search Result", JOptionPane.INFORMATION_MESSAGE);
        }
    }

